package kr.or.ddit.dcum.vo;

public class BambooVO {
	
	private String sec_num;
	private String sec_cont;
	private String sec_date;
	private String sec_title;
	private String sec_writer;
	private String mem_id;
	
	
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
	public String getSec_num() {
		return sec_num;
	}
	public void setSec_num(String sec_num) {
		this.sec_num = sec_num;
	}
	
	public String getSec_cont() {
		return sec_cont;
	}
	public void setSec_cont(String sec_cont) {
		this.sec_cont = sec_cont;
	}
	
	public String getSec_date() {
		return sec_date;
	}
	public void setSec_date(String sec_date) {
		this.sec_date = sec_date;
	}
	
	public String getSec_title() {
		return sec_title;
	}
	public void setSec_title(String sec_title) {
		this.sec_title = sec_title;
	}
	
	public String getSec_writer() {
		return sec_writer;
	}
	public void setSec_writer(String sec_writer) {
		this.sec_writer = sec_writer;
	}

}
