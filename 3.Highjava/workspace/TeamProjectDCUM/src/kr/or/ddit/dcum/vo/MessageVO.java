package kr.or.ddit.dcum.vo;

public class MessageVO {
	
	//쪽지 기능 구현
	private int mes_num;	
	private String mes_date;
	private String mes_cont;
	private String bal_mem_id;
	private String su_mem_id;
	
	
	public int getMes_num() {
		return mes_num;
	}
	public void setMes_num(int mes_num) {
		this.mes_num = mes_num;
	}
	public String getMes_date() {
		return mes_date;
	}
	public void setMes_date(String mes_date) {
		this.mes_date = mes_date;
	}
	public String getMes_cont() {
		return mes_cont;
	}
	public void setMes_cont(String mes_cont) {
		this.mes_cont = mes_cont;
	}
	public String getBal_mem_id() {
		return bal_mem_id;
	}
	public void setBal_mem_id(String bal_mem_id) {
		this.bal_mem_id = bal_mem_id;
	}
	public String getSu_mem_id() {
		return su_mem_id;
	}
	public void setSu_mem_id(String su_mem_id) {
		this.su_mem_id = su_mem_id;
	}
	
	
	
	
	
	
}
